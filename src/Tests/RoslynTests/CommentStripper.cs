class CommentStripper : CSharpSyntaxRewriter
{
    public static SyntaxTree Strip(string code)
    {
        var tree = CSharpSyntaxTree.ParseText(code);
        var stripper = new CommentStripper();
        var newRoot = stripper.Visit(tree.GetRoot());
        return tree.WithRootAndOptions(newRoot, tree.Options);
    }

    public override bool VisitIntoStructuredTrivia => true;

    public override SyntaxTrivia VisitTrivia(SyntaxTrivia trivia)
    {
        if (trivia.IsKind(SyntaxKind.DisabledTextTrivia))
        {
            return SyntaxFactory.Whitespace("");
        }
        if (trivia.IsKind(SyntaxKind.SingleLineCommentTrivia))
        {
            if (trivia.ToString() != "// <auto-generated />")
            {
                return SyntaxFactory.Whitespace("");
            }
        }

        if (trivia.IsKind(SyntaxKind.DisabledTextTrivia))
        {
            return SyntaxFactory.Whitespace("");
        }

        return base.VisitTrivia(trivia);
    }
    public override SyntaxNode? VisitIfDirectiveTrivia(IfDirectiveTriviaSyntax node)
    {
        EndIfDirectiveTriviaSyntax correspondingEndNode=  FindEndIfForIfDirectiveTriviaSyntax(node);
        return null;
    }

    EndIfDirectiveTriviaSyntax FindEndIfForIfDirectiveTriviaSyntax(IfDirectiveTriviaSyntax node)
    {
        var parent = node.Parent;
        var index = parent!.ChildNodes().inIndexOf(node);
        for (int i = index + 1; i < parent.ChildNodes().Count; i++)
        {
            if (parent.ChildNodes()[i] is EndIfDirectiveTriviaSyntax endIf)
            {
                return endIf;
            }
        }
        return null;
    }

    public override SyntaxNode? VisitElifDirectiveTrivia(ElifDirectiveTriviaSyntax node)
    {
        return null;
    }

    public override SyntaxNode? VisitElseDirectiveTrivia(ElseDirectiveTriviaSyntax node)
    {
        return null;
    }

    public override SyntaxNode? VisitEndIfDirectiveTrivia(EndIfDirectiveTriviaSyntax node)
    {
        return null;
    }

}

public static class DirectiveTriviaHelper
{
    public static List<DirectiveTriviaSyntax> FindRelatedDirectives(IfDirectiveTriviaSyntax ifDirective)
    {
        var relatedDirectives = new List<DirectiveTriviaSyntax> { ifDirective };
        var parent = ifDirective.Parent;
        var index = parent.ChildNodes().IndexOf(ifDirective);

        for (int i = index + 1; i < parent.ChildNodes().Count; i++)
        {
            var node = parent.ChildNodes()[i];
            if (node is EndIfDirectiveTriviaSyntax endIf)
            {
                relatedDirectives.Add(endIf);
                break;
            }
            if (node is ElifDirectiveTriviaSyntax elif)
            {
                relatedDirectives.Add(elif);
            }
            if (node is ElseDirectiveTriviaSyntax @else)
            {
                relatedDirectives.Add(@else);
            }
        }

        return relatedDirectives;
    }
}
